package air_system;

public class Person {
	protected String Name;
	protected String adress;
	public void set_name(String name) {
		Name=name;
	}
	public String get_name() {
		return Name;
	}

	public void set_adress(String d) {
		adress=d;
	}
	public String get_adress() {
		return adress;
	}
}
